package com.example.ecommerce.data.model;

public class AdminOrders
{
    private String name, city,date, time, totalAmount, pincode, status, address, phone;

    public AdminOrders()
    {
    }

    public AdminOrders(String name, String city, String date, String time, String totalAmount, String pincode, String status, String address, String phone) {
        this.name = name;
        this.city = city;
        this.date = date;
        this.time = time;
        this.totalAmount = totalAmount;
        this.pincode = pincode;
        this.status = status;
        this.address = address;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(String totalAmount) {
        this.totalAmount = totalAmount;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
